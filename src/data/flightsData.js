export const generateSeatMap = () => {
  const rows = [];
  const seatLetters = ['A', 'B', 'C', 'D', 'E', 'F'];

  for (let row = 1; row <= 30; row++) {
    const seats = seatLetters.map(letter => ({
      id: `${row}${letter}`,
      row,
      letter,
      status: Math.random() < 0.35 ? 'occupied' : (Math.random() < 0.1 ? 'selected' : 'available'),
      type: row <= 4 ? 'business' : row <= 7 ? 'extra-legroom' : 'economy',
      price: row <= 4 ? 800 : row <= 7 ? 150 : 0,
    }));
    rows.push({ row, seats });
  }
  return rows;
};

export const getFlightById = (id) => flights.find(f => f.id === id);

export const searchFlights = ({ from, to, date }) => {
  return flights.filter(f => {
    const fromMatch = !from || f.from.code === from || f.from.city.includes(from);
    const toMatch = !to || f.to.code === to || f.to.city.includes(to);
    return fromMatch && toMatch;
  });
};
